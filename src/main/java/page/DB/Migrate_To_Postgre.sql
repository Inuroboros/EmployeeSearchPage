CREATE TABLE Employees (
                           id int,
                           username varchar(255),
                           email varchar(255),
                           password varchar(255)
);

CREATE TABLE Portfolios (
                           name varchar(255),
                           Birthday varchar(255),
                           Gender varchar(255),
                           experience varchar(255),
                           speciality varchar(255),
                           lastWork varchar(255),
                           username varchar(255)
);